          DO 120 NPP=1,NVNP
            NKK=NPVB(NPP)
            RSVAB(NPP,4)=BFLX(NKK,2)
  120     CONTINUE
          WRITE(16,1100)
          WRITE(16,1110) (RSVAB(NPP,4),NPP=1,NVNP)
          WRITE(16,1120) (INDRS(NPP,1),NPP=1,NVNP)
          WRITE(16,1125) (INDRS(NPP,2),NPP=1,NVNP)

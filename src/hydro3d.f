      DIMENSION V(MAXNP,3),H(MAXNP),HT(MAXNP), TH(MAXEL,8)
      DIMENSION BFLX(MAXBNP,2),NPVB(MXVNP),INDRS(MXVNP,3),RSVAB(MXVNP,4)

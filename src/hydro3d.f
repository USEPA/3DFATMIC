c         IF(ISP.EQ.1)GOTO 900
c         uskfct=1.0d0/(alpha*hnp)**(alamda*3+2)
c         dth=alpha*alamda*(wcs-wcr)*(alpha*hnp)**(-alamda-1.0d0)

      IF(KPR.LE.0) RETURN
C ----- print diagnostic flow information
        KDIAG=KDIAG+1
        KDIA=KDIAG-1
        WRITE(16,1000) KDIA,TIME,DELT,ITIM,(FRATE(I),FLOW(I),TFLOW(I),
     >              I=1,9)

C
c      SUBROUTINE SPROP(AKHC,TH,DTH,X,H,C,IE,PROP,DINTS,RHOMU,SPP,
      SUBROUTINE SPROP(AKHC,TH,DTH,X,H,C,IE,PROP,DINTS,SPP,
     >                 IRHO,IQUAR,KSP,cnstkr)

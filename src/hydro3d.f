     I           SATKYZ,CKG,ISP,IRHO,DINTS,RHOTYP,cnstkr,
     O           TH,DTH,AKHCX,AKHCY,AKHCZ,AKHCXY,AKHCXZ,AKHCYZ,RHOM)

c         IF(ISP.EQ.1)GOTO 900
c         uskfct=(th/wcs)**(2.0d0*b+3.0)
c         dth=-1.0d0/b*wcs/hcs*(-hnp/hcs)**(-1.0d0/b-1.0d0)
C
c ------ moisture content and Kr for adaepa hypothetical case

     8 '8. ARTIFICIAL SOURCES/SINKS . . . .',3(1PD12.4,2X)/
     9 '9. INCREASE IN WATER CONTENT . . . ',3(1PD12.4,2X)/
     A ' *** NOTE: (+) = OUT FROM, (-) = INTO THE REGION. '//)
 1100 FORMAT(/' RAINFALL-SEEPAGE NODAL FLOWS (L**3/T)')

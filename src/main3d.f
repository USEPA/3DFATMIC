          CALL TSFLOW
     >      (BFLXt(1,1,K),X,IE,C(1,K),F(1,1,K),TH,RKD(1,K),TRANC(1,K),
     >       DCOSB,ISB,NPBB, SOSt,ISTYPt(1,K),LESt, WSSt,IWTYPt(1,K),

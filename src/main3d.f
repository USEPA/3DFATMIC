        KFLOW=0
        CALL FSFLOW(X,IE,NLRL,LRL, H,HP,V,TH,DTH,AKHC(1,1,7),PROPf(1,7),
     >     ISTYPT,SOST,IWTYPT,WSST,C,
     1     BFLXf,DCOSB,ISB,NPBB, LESf,SOSf,ISTYPf,WSSf,IWTYPf,NPWf,
